// clang-format off
#define test_alun_b_vx(op, in0, in1, ref)                          \
{                                                                  \
  uint8_t dut[VLENB] __attribute__((aligned(64))) = {0xcc};        \
  vdup_h_x(v0, in0);                                               \
  __asm__ __volatile__(ARGS_F_A_A_A(op, v2, v0, %0) : : "r"(in1)); \
  vst_b_x(v2, dut);                                                \
  if (ref != dut[0]) {                                             \
    printf("**error(%d)[%s] %04x %04x : %02x %02x\n",              \
        __LINE__, op, in0, in1, ref, dut[0]);                      \
    exit(-1);                                                      \
  }                                                                \
}

